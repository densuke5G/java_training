package question07;

public class Question07_01 {

	public static void main(String[] args) {
		int answer1 = 12 + 3;
		int answer2 = 12 - 3;
		int answer3 = 12 * 3;
		int answer4 = 12 / 3;
		int answer5 = 4 % 3;
		
		
		System.out.println("12+3は" + answer1 + "です");
		System.out.println("12-3は" + answer2 + "です");
		System.out.println("12x3は" + answer3 + "です");
		System.out.println("12÷3は" + answer4 + "です");
		System.out.println("4÷3の余りは" + answer5 + "です");

	}

}
